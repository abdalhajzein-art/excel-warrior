let sessionHistory = [];

/* -------------------------------------------------------
   طبقة فهم النية (Intent Engine)
------------------------------------------------------- */
function detectIntent(message) {
  const msg = message.toLowerCase();

  if (msg.includes("ضيف") || msg.includes("إضافة") || msg.includes("عمود")) return "add_column";
  if (msg.includes("احذف") || msg.includes("حذف") || msg.includes("شيل")) return "delete_column";
  if (msg.includes("عدل") || msg.includes("تعديل") || msg.includes("غير")) return "modify_data";
  if (msg.includes("ولدلي") || msg.includes("انشئ") || msg.includes("ملف جديد") || msg.includes("نظام")) return "create_file";
  if (msg.includes("رجعلي") || msg.includes("عطيني الملف") || msg.includes("النسخة الجديدة") || msg.includes("الملف المعدل")) return "request_modified_file";

  return "chat";
}

export default async function handler(req, res) {
  if (req.method !== "POST") return res.status(405).end();

  try {
    const { message, reset, excelContent } = req.body;
    const apiKey = process.env.GROQ_API_KEY;

    if (!apiKey) {
      return res.status(500).json({
        reply: "⚠️ GROQ_API_KEY غير موجود ضمن المتغيرات البيئية"
      });
    }

    if (reset === true) {
      sessionHistory = [];
      return res.status(200).json({ reply: "🔄 تم بدء جلسة جديدة." });
    }

    const intent = detectIntent(message);

    /* -------------------------------------------------------
       سياق الملف (Base64 أو JSON)
------------------------------------------------------- */
    let fileContext = "";
    if (excelContent) {
      fileContext = `
      المستخدم أرفق ملف Excel بصيغة Base64 أو JSON.

      مهمتك كخبير جداول عام ومتكيّف:
      - لا تفكّ الملف تقنياً.
      - افهم نوع الملف من شكل البيانات أو من كلام المستخدم.

      طبقة الذكاء العميقة:
      1) اكتشاف نوع النظام تلقائياً:
         - إذا وجدت أيام/حضور/غياب → نظام حضور وغياب.
         - إذا وجدت أسعار/كميات/ربح → نظام بيع وشراء.
         - إذا وجدت رواتب/بدلات/خصومات → نظام رواتب.
         - إذا وجدت كميات/حد أدنى/حد أعلى → نظام مخزون.
         - إذا وجدت مهام/حالة/تقدم → نظام مشاريع.
         - إذا وجدت فواتير/دفعات → نظام مالي.

      2) اكتشاف الهيدر تلقائياً:
         - أي صف يحتوي أسماء أعمدة هو الهيدر.
         - الأعمدة التي تحتوي نصوص عامة هي صفوف تعليمية.

      3) اكتشاف الصفوف الملخصة:
         - أي صف يحتوي "إجمالي" أو "ملخص" هو صف ملخص.

      4) اكتشاف الأعمدة الأساسية:
         - الأعمدة النصية (اسم، قسم، موظف…)
         - الأعمدة الرقمية (قيم، أرقام…)
         - الأعمدة الزمنية (يوم 1، يوم 2…)
         - الأعمدة الحسابية (حضور، غياب، نسبة…)
         - الأعمدة المالية (راتب، خصم…)
         - الأعمدة التجارية (سعر، كمية، ربح…)

      5) اقتراح مكان التعديل:
         - إذا كان العمود إداري → بعد الغياب أو التأخير.
         - إذا كان العمود تجاري → بعد السعر أو الكمية.
         - إذا كان العمود مالي → بعد الراتب أو الخصم.
         - إذا كان العمود مخزون → بعد الكمية.
         - إذا كان العمود مشاريع → بعد حالة المهمة.

      6) التحذير الذكي:
         - أي تعديل على الأعمدة الحسابية قد يؤثر على الصيغ.
         - أي حذف لأعمدة الأيام قد يخرب نسبة الحضور.
         - أي تعديل على صفوف الملخص قد يخرب الحسابات.

      7) الأسئلة الذكية:
         - اسأل عن اسم العمود الجديد.
         - اسأل عن نوع البيانات.
         - اسأل عن مكان الإضافة.
         - اسأل عن نطاق التعديل.

      8) الموافقة قبل التنفيذ:
         - قبل أي تنفيذ، اطلب تأكيد المستخدم.
         - استخدم جملة طبيعية مثل:
           "تمام… إذا موافق، حضّرلك النسخة الجديدة."
      `;
    }

    sessionHistory.push({
      role: "user",
      content: `
      نية المستخدم: ${intent}
      رسالة المستخدم:
      ${message}

      سياق الملف:
      ${fileContext}
      `
    });

    const messagesToSend = [
      {
        role: "system",
        content: `
        أنت مساعد ذكي عام ومتكيّف، موظّف خبير ومحارب شغل.
        إنسانيتك فقط في أسلوب الكلام.

        قواعد أساسية:
        - رد بأسلوب طبيعي يشبه الإنسان.
        - ناقش، اسأل، واقترح، واشتغل كخبير جداول عام.
        - لا تنشئ هوية أو اسم لنفسك.
        - لا تتحدث عن سيرفرات أو تنفيذ خارجي.
        - لا تنفّذ أي تعديل بنفسك.
        - لا تولّد ملف Excel بنفسك.

        إذا طلب المستخدم "النسخة الجديدة" أو "الملف المعدل":
        استخدم جملة طبيعية مثل:
        "تمام… خليني حضّرلك النسخة الجديدة."
        "جاهز… رح جهّزلك النسخة المعدّلة."
        "تمام… رح أرتّبلك النسخة الجديدة."

        طبقة الفهم العامة:
        - افهم نوع الملف من كلام المستخدم أو من شكل البيانات.
        - إذا كان إداري → طبّق منطق الحضور والغياب.
        - إذا كان تجاري → طبّق منطق البيع والشراء.
        - إذا كان مخزون → طبّق منطق الكميات.
        - إذا كان مالي → طبّق منطق الرواتب.
        - إذا كان مشاريع → طبّق منطق المهام.

        طبقة الفهم العميقة:
        - اكتشف الهيدر الحقيقي.
        - اكتشف الصفوف التعليمية.
        - اكتشف الصفوف الملخصة.
        - اكتشف الأعمدة الأساسية.
        - اكتشف الأعمدة الحسابية.
        - اكتشف الأعمدة الملخصة.
        - اقترح مكان التعديل المناسب.
        - نبّه قبل أي تعديل يؤثر على الصيغ.

        طبقة الأسئلة الذكية:
        - اسأل عن اسم العمود الجديد.
        - اسأل عن نوع البيانات.
        - اسأل عن مكان الإضافة.
        - اسأل عن نطاق التعديل.

        طبقة الموافقة:
        - قبل أي تنفيذ، اطلب تأكيد المستخدم.
        `
      },
      ...sessionHistory
    ];

    const response = await fetch("https://api.groq.com/openai/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${apiKey}`
      },
      body: JSON.stringify({
        model: "openai/gpt-oss-120b",
        messages: messagesToSend,
        temperature: 0.4
      })
    });

    const data = await response.json();
    const aiReply = data?.choices?.[0]?.message?.content;

    if (aiReply) {
      sessionHistory.push({
        role: "assistant",
        content: aiReply
      });

      return res.status(200).json({
        reply: aiReply
      });
    } else {
      return res.status(500).json({
        reply: "⚠️ الخطأ الكامل من Groq: " + JSON.stringify(data)
      });
    }

  } catch (error) {
    return res.status(500).json({
      reply: "⚠️ خطأ في الاتصال: " + error.message
    });
  }
        }
